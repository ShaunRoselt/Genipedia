function ensureSisterProjectsStyles() {
  if (document.getElementById(SISTER_PROJECTS_STYLE_ELEMENT_ID)) {
    return;
  }

  const styleElement = document.createElement('style');
  styleElement.id = SISTER_PROJECTS_STYLE_ELEMENT_ID;
  styleElement.textContent = SISTER_PROJECTS_STYLES;
  document.head.appendChild(styleElement);
}

class SisterProjects extends HTMLElement {
  connectedCallback() {
    if (this.__rendered) return;
    this.__rendered = true;
    ensureSisterProjectsStyles();
    this.innerHTML = SISTER_PROJECTS_TEMPLATE;
  }
}

if (!customElements.get('sister-projects')) {
  customElements.define('sister-projects', SisterProjects);
}
